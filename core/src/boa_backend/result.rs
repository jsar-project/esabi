use alloc::{ffi::NulError, string::String as StdString};
use core::{
    error::Error as StdError,
    ffi::FromBytesWithNulError,
    fmt::{self, Display, Formatter},
    str::Utf8Error,
};

#[cfg(feature = "std")]
use std::io::Error as IoError;

pub type Result<T> = core::result::Result<T, Error>;

#[derive(Debug)]
#[non_exhaustive]
pub enum Error {
    Allocation,
    InvalidString(NulError),
    InvalidCStr(FromBytesWithNulError),
    Utf8(Utf8Error),
    #[cfg(feature = "std")]
    Io(IoError),
    Exception,
    FromJs {
        from: &'static str,
        to: &'static str,
        message: Option<StdString>,
    },
    IntoJs {
        from: &'static str,
        to: &'static str,
        message: Option<StdString>,
    },
    Unsupported {
        message: StdString,
    },
    Unknown,
}

impl Error {
    pub fn is_exception(&self) -> bool {
        matches!(self, Self::Exception)
    }

    pub fn new_from_js(from: &'static str, to: &'static str) -> Self {
        Self::FromJs {
            from,
            to,
            message: None,
        }
    }

    pub fn new_from_js_message<M>(from: &'static str, to: &'static str, message: M) -> Self
    where
        StdString: From<M>,
    {
        Self::FromJs {
            from,
            to,
            message: Some(message.into()),
        }
    }

    pub fn new_into_js(from: &'static str, to: &'static str) -> Self {
        Self::IntoJs {
            from,
            to,
            message: None,
        }
    }

    pub fn new_into_js_message<M>(from: &'static str, to: &'static str, message: M) -> Self
    where
        StdString: From<M>,
    {
        Self::IntoJs {
            from,
            to,
            message: Some(message.into()),
        }
    }

    pub fn unsupported<M>(message: M) -> Self
    where
        StdString: From<M>,
    {
        Self::Unsupported {
            message: message.into(),
        }
    }

    pub fn is_from_js(&self) -> bool {
        matches!(self, Self::FromJs { .. })
    }

    pub fn is_into_js(&self) -> bool {
        matches!(self, Self::IntoJs { .. })
    }
}

impl Display for Error {
    fn fmt(&self, f: &mut Formatter<'_>) -> fmt::Result {
        match self {
            Error::Allocation => write!(f, "Allocation failed while creating object"),
            Error::InvalidString(error) => {
                write!(f, "String contained internal null bytes: {error}")
            }
            Error::InvalidCStr(error) => write!(f, "CStr didn't end in a null byte: {error}"),
            Error::Utf8(error) => write!(f, "Conversion from string failed: {error}"),
            #[cfg(feature = "std")]
            Error::Io(error) => write!(f, "IO Error: {error}"),
            Error::Exception => write!(f, "Exception generated by Boa"),
            Error::FromJs { from, to, message } => {
                write!(f, "Error converting from js '{from}' into type '{to}'")?;
                if let Some(message) = message {
                    if !message.is_empty() {
                        write!(f, ": {message}")?;
                    }
                }
                Ok(())
            }
            Error::IntoJs { from, to, message } => {
                write!(f, "Error converting from '{from}' into js '{to}'")?;
                if let Some(message) = message {
                    if !message.is_empty() {
                        write!(f, ": {message}")?;
                    }
                }
                Ok(())
            }
            Error::Unsupported { message } => write!(f, "Unsupported in Boa backend: {message}"),
            Error::Unknown => write!(f, "Boa backend created an unknown error"),
        }
    }
}

impl StdError for Error {}

macro_rules! from_impls {
    ($($type:ty => $variant:ident,)*) => {
        $(
            impl From<$type> for Error {
                fn from(error: $type) -> Self {
                    Error::$variant(error)
                }
            }
        )*
    };
}

from_impls! {
    NulError => InvalidString,
    FromBytesWithNulError => InvalidCStr,
    Utf8Error => Utf8,
}

#[cfg(feature = "std")]
from_impls! {
    IoError => Io,
}
